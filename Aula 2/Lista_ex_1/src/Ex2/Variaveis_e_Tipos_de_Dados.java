/* Exercício 2: Variáveis e Tipos de Dados
 * Crie um programa que declare variáveis de diferentes tipos (int, double, boolean, char) e as inicialize com
 * valores apropriados. Em seguida, imprima esses valores no console.
*/

package Ex2;

public class Variaveis_e_Tipos_de_Dados {
    public static void main(String[] args){
        int Inteiro = 1;
        double Double = 2.256;
        boolean Boolean = true;
        char Character = 'A';

        System.out.println("Int: " + Inteiro);
        System.out.println("Double: " + Double);
        System.out.println("Boolean: " + Boolean);
        System.out.println("Char: " + Character);
    }
}
