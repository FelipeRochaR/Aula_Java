/* Exercício 5: Estruturas de Repetição
 * Escreva um programa que imprima todos os números de 1 a 10 usando um loop for.
*/

package Ex5;

public class Estruturas_de_Repeticao {
    public static void main(String[] args){
        for(int i = 1; i <= 10; i++){           //Enquanto i for menos ou igual a 10 vai imprimir os numeros.
            System.out.println(" " + i);
        }
    }
}
